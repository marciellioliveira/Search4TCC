    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nomes = "";
        Conexao conex = new Conexao();
        String sql =  "SELECT * FROM cad_temas_tcc WHERE NOME_TEMA = '"+jComboBox5.getSelectedItem()+"';";
        String sql2 = "SELECT * FROM cad_grupos_tcc WHERE TEMAS = '"+jComboBox5.getSelectedItem()+"';";
        
        ResultSet resultados = conex.executarConsulta (sql);
        ResultSet resultados2 = conex.executarConsulta(sql2);
        
        try {
            
            String sistema="", resumo="", palavrachave="", datainicio="", dataentrega="", prof_resp="";
            
            while (resultados.next()) {
                           
              sistema=resultados.getString ("nome_sistema");
              resumo=resultados.getString ("sobre_sistema");
              palavrachave=resultados.getString ("palavra_chave");
              datainicio=resultados.getString ("inicio");
              dataentrega=resultados.getString ("entrega");
              prof_resp=resultados.getString ("prof_orient");
            }
            
            jLabel17.setText(sistema);
            jTextArea1.setText(resumo);
            jLabel23.setText(palavrachave);
            jLabel24.setText(datainicio);
            jLabel25.setText(dataentrega);
            jLabel2.setText(prof_resp);
            
            
        } catch (SQLException ex) {
            Logger.getLogger(ArquivoTCCTemas2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            while (resultados2.next()) {
                
                if(nomes.equals("")) {
                    nomes = resultados2.getString("aluno");
                } else {
                    nomes = nomes+", "+resultados2.getString("aluno");
                }
            }
            alunos.setText(nomes);
        } catch (SQLException e) {
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

