    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        Conexao conex = new Conexao();
        String sql="";
        if(jComboBox3.getSelectedItem().equals("Todos os Períodos")) {
            sql =  "SELECT NOME_TEMA FROM cad_temas_tcc WHERE ANO_DESENVOLVIDO = '"+jComboBox1.getSelectedItem()+"' AND curso = '"+jComboBox2.getSelectedItem()+"' AND instituicao ='"+jComboBox4.getSelectedItem()+"';";
        } else {
            sql =  "SELECT NOME_TEMA FROM cad_temas_tcc WHERE ANO_DESENVOLVIDO = '"+jComboBox1.getSelectedItem()+"' AND curso = '"+jComboBox2.getSelectedItem()+"' AND periodo = '"+jComboBox3.getSelectedItem()+"' AND instituicao ='"+jComboBox4.getSelectedItem()+"';";
        }
        
        
        ResultSet resultados = conex.executarConsulta (sql);
        
           
            try {
                if(!resultados.isBeforeFirst()) {
                    jComboBox5.removeAllItems();
                  JOptionPane.showMessageDialog(null,"O tema ainda não foi cadastrado!");
                } else {
                    while ( resultados.next()){
                         jComboBox5.addItem(resultados.getString("NOME_TEMA"));
                     }
                }
            } catch (SQLException ex) {
                Logger.getLogger(ArquivoTCCTemas2.class.getName()).log(Level.SEVERE, null, ex);
            }

    }//GEN-LAST:event_jButton3ActionPerformed

